export type CommunityStatus = 'Aligned' | 'Partial' | 'Gap' | 'Not found';
export type CommunityRole = 'Workstation' | 'Server' | 'DC';
export type CommunityRoleChoice = 'Auto' | CommunityRole;
export type CommunityOperatingSystem = 'Windows 10' | 'Windows 11' | 'Windows Server 2016' | 'Windows Server 2019' | 'Windows Server 2022' | 'Windows Server 2025' | 'Windows (unspecified)';
export type CommunityOperatingSystemChoice = 'Auto' | CommunityOperatingSystem;
export type CommunityFramework = 'Microsoft' | 'CIS' | 'NIST CSF';

export interface CommunityEventReference {
  id: number;
  name: string;
}

export interface CommunityControl {
  id: string;
  category: string;
  subcategory: string;
  expected: string;
  why: string;
  remediation: string;
  frameworks: readonly CommunityFramework[];
  events: readonly CommunityEventReference[];
  roles?: readonly CommunityRole[];
}

export interface CommunityFinding extends CommunityControl {
  actual: string;
  status: CommunityStatus;
}

export interface CommunityParseStats {
  linesRead: number;
  recognisedSettings: number;
  duplicateSettings: number;
  categoriesSeen: number;
}

export interface CommunityAssessment {
  assetName: string;
  role: CommunityRole;
  operatingSystem: CommunityOperatingSystem;
  roleWasDetected: boolean;
  operatingSystemWasDetected: boolean;
  findings: CommunityFinding[];
  aligned: number;
  partial: number;
  gap: number;
  notFound: number;
  score: number;
  parseStats: CommunityParseStats;
  notices: string[];
}

export interface CommunityAssessmentResult {
  assessment: CommunityAssessment | null;
  errors: string[];
  warnings: string[];
}

