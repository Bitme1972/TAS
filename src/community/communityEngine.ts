const categoryAliases = new Map<string, string>([
  ['system', 'System'],
  ['logon/logoff', 'Logon/Logoff'],
  ['object access', 'Object Access'],
  ['privilege use', 'Privilege Use'],
  ['detailed tracking', 'Detailed Tracking'],
  ['policy change', 'Policy Change'],
  ['account management', 'Account Management'],
  ['ds access', 'DS Access'],
  ['directory service access', 'DS Access'],
  ['account logon', 'Account Logon']
]);

const settingAtEndPattern = /(No\s+Auditing|Success\s+and\s+Failure|Failure\s+and\s+Success|Success|Failure)\s*$/i;
const guidAtEndPattern = /\s*\{[0-9a-f-]{30,40}\}\s*$/i;

function normaliseSetting(value: string): string {
  const clean = value.trim().replace(/\s+/g, ' ');
  if (/^failure and success$/i.test(clean)) return 'Success and Failure';
  if (/^no auditing$/i.test(clean)) return 'No Auditing';
  if (/^success and failure$/i.test(clean)) return 'Success and Failure';
  if (/^success$/i.test(clean)) return 'Success';
  if (/^failure$/i.test(clean)) return 'Failure';
  return clean;
}

function compareSetting(expected: string, actual: string): CommunityStatus {
  if (!actual) return 'Not found';
  if (expected === actual) return 'Aligned';
  if (actual === 'No Auditing') return 'Gap';
  if (actual === 'Success and Failure' && (expected === 'Success' || expected === 'Failure')) return 'Partial';
  if (expected === 'Success and Failure' && (actual === 'Success' || actual === 'Failure')) return 'Partial';
  return 'Partial';
}

function detectRole(raw: string): CommunityRole | null {
  if (/\b(domain controller|directory service changes|ds access|active directory)\b/i.test(raw)) return 'DC';
  if (/\bwindows server\b/i.test(raw)) return 'Server';
  if (/\bwindows (?:10|11)\b/i.test(raw)) return 'Workstation';
  return null;
}

function detectOperatingSystem(raw: string): CommunityOperatingSystem | null {
  const candidates: Array<[RegExp, CommunityOperatingSystem]> = [
    [/\bwindows server 2025\b/i, 'Windows Server 2025'],
    [/\bwindows server 2022\b/i, 'Windows Server 2022'],
    [/\bwindows server 2019\b/i, 'Windows Server 2019'],
    [/\bwindows server 2016\b/i, 'Windows Server 2016'],
    [/\bwindows 11\b/i, 'Windows 11'],
    [/\bwindows 10\b/i, 'Windows 10']
  ];
  return candidates.find(([pattern]) => pattern.test(raw))?.[1] ?? null;
}

function sanitiseAssetName(value: string): string {
  return value.trim().replace(/[<>"'`]/g, '').replace(/\s+/g, ' ').slice(0, 80) || 'COMMUNITY-ASSET';
}

function extractAssetNames(lines: string[]): string[] {
  const values: string[] = [];
  const pattern = /^\s*(?:Asset|Host|Hostname|Computer|Computer Name|Machine|Machine Name|Server|DC)\s*[:=]\s*(.+?)\s*$/i;
  for (const line of lines) {
    const match = line.match(pattern);
    if (match?.[1]) values.push(sanitiseAssetName(match[1]));
  }
  return [...new Set(values.map(value => value.toLowerCase()))].map(lower => values.find(value => value.toLowerCase() === lower) as string);
}

export function assessCommunityEvidence(
  raw: string,
  requestedRole: CommunityRoleChoice = 'Auto',
  requestedOperatingSystem: CommunityOperatingSystemChoice = 'Auto'
): CommunityAssessmentResult {
  const errors: string[] = [];
  const warnings: string[] = [];
  const cleanRaw = raw.replace(/^\uFEFF/, '');
  const evidenceBytes = new TextEncoder().encode(cleanRaw).byteLength;

  if (!cleanRaw.trim()) return { assessment: null, errors: ['Add one auditpol evidence file or paste its text before running the assessment.'], warnings };
  if (evidenceBytes > COMMUNITY_MAX_EVIDENCE_BYTES) errors.push('Community accepts one text evidence item up to 1 MB. Reduce the file or use TAS Professional for larger assessments.');

  const lines = cleanRaw.replace(/\r\n?/g, '\n').split('\n');
  const assetNames = extractAssetNames(lines);
  if (assetNames.length > 1) errors.push(`Community accepts one asset per assessment. This evidence appears to contain ${assetNames.length} assets.`);

  const settings = new Map<string, string>();
  const categoriesSeen = new Set<string>();
  let duplicateSettings = 0;
  let category = '';

  for (const line of lines) {
    const trimmed = line.trim();
    const categoryMatch = categoryAliases.get(trimmed.toLowerCase().replace(/:$/, ''));
    if (categoryMatch) {
      category = categoryMatch;
      categoriesSeen.add(categoryMatch);
      continue;
    }

    const settingMatch = line.match(settingAtEndPattern);
    if (!settingMatch || !category) continue;
    const actual = normaliseSetting(settingMatch[1]);
    const prefix = line.slice(0, settingMatch.index).trim().replace(guidAtEndPattern, '').trim();
    if (!prefix || /^category\/subcategory$/i.test(prefix)) continue;
    const key = `${category}::${prefix}`.toLowerCase();
    if (settings.has(key)) duplicateSettings += 1;
    settings.set(key, actual);
  }

  if (!settings.size) errors.push('No recognisable Advanced Audit Policy settings were found. Use text produced by auditpol /get /category:* or load the safe demonstration.');
  if (errors.length) return { assessment: null, errors, warnings };

  const detectedRole = detectRole(cleanRaw);
  const role = requestedRole === 'Auto' ? (detectedRole ?? 'Workstation') : requestedRole;
  const detectedOperatingSystem = detectOperatingSystem(cleanRaw);
  const operatingSystem = requestedOperatingSystem === 'Auto'
    ? (detectedOperatingSystem ?? (role === 'Workstation' ? 'Windows 11' : 'Windows Server 2022'))
    : requestedOperatingSystem;

  if (requestedRole === 'Auto' && !detectedRole) warnings.push(`Asset role was not present in the evidence. Community used ${role}; review and change it if needed.`);
  if (requestedOperatingSystem === 'Auto' && !detectedOperatingSystem) warnings.push(`Operating system was not present in the evidence. Community used ${operatingSystem}; review and change it if needed.`);
  if (duplicateSettings) warnings.push(`${duplicateSettings} duplicate audit setting${duplicateSettings === 1 ? ' was' : 's were'} found; the last value was assessed.`);

  const controls = communityControls.filter(control => !control.roles || control.roles.includes(role));
  const findings = controls.map(control => {
    const actual = settings.get(`${control.category}::${control.subcategory}`.toLowerCase()) || '';
    return { ...control, actual: actual || 'Not found', status: compareSetting(control.expected, actual) };
  });

  const aligned = findings.filter(item => item.status === 'Aligned').length;
  const partial = findings.filter(item => item.status === 'Partial').length;
  const gap = findings.filter(item => item.status === 'Gap').length;
  const notFound = findings.filter(item => item.status === 'Not found').length;
  const score = findings.length ? Math.round(((aligned + partial * 0.5) / findings.length) * 100) : 0;
  const notices = [
    `Community assessed ${findings.length} selected controls for one ${role}.`,
    'Framework labels are selected assurance perspectives, not certification or a complete benchmark assessment.',
    'Raw evidence remains in this browser session and is not included in the downloaded report.'
  ];

  return {
    assessment: {
      assetName: assetNames[0] ?? 'COMMUNITY-ASSET',
      role,
      operatingSystem,
      roleWasDetected: requestedRole === 'Auto' && Boolean(detectedRole),
      operatingSystemWasDetected: requestedOperatingSystem === 'Auto' && Boolean(detectedOperatingSystem),
      findings,
      aligned,
      partial,
      gap,
      notFound,
      score,
      parseStats: {
        linesRead: lines.length,
        recognisedSettings: settings.size,
        duplicateSettings,
        categoriesSeen: categoriesSeen.size
      },
      notices
    },
    errors,
    warnings
  };
}

export async function sha256Evidence(raw: string): Promise<string> {
  const bytes = new TextEncoder().encode(raw.replace(/^\uFEFF/, ''));
  if (!globalThis.crypto?.subtle) return 'SHA-256 unavailable in this browser';
  const digest = await globalThis.crypto.subtle.digest('SHA-256', bytes);
  return Array.from(new Uint8Array(digest), value => value.toString(16).padStart(2, '0')).join('');
}

